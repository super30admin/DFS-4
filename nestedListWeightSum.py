class Solution:
    def depthSum(self, nestedList: List[NestedInteger]) -> int:
        if not nestedList:
            return 0

        q = deque([])
        result = 0
        for ele in nestedList:
            q.append(ele)

        level = 1
        while q:
            for i in range(len(q)):
                curr = q.popleft()
                if curr.isInteger():
                    result += level * curr.getInteger()
                else:
                    for ele in curr.getList():
                        q.append(ele)
            level += 1

        return result
