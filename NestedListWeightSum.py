from collections import deque


class Solution:
    def depthSum(self, nestedList: List[NestedInteger]) -> int:
        queue = deque()
        sum = 0
        for el in nestedList:
            queue.append((el, 1))
        while queue:
            el, level = queue.popleft()
            if el.isInteger():
                sum += el.getInteger() * level
            else:
                for el2 in el.getList():
                    queue.append((el2, level + 1))
        return sum


sol = Solution()
print(sol.depthSum([[1, 1], 2, [1, 1]]))
